class SlotLayoutConfig {
  final double topOffset;
  final double leftShiftFactor;
  final double distanceReductionFactor;
  final double horizontalSpacingFactor;
  final double buttonReductionFactor;

  const SlotLayoutConfig({
    required this.topOffset,
    required this.leftShiftFactor,
    required this.distanceReductionFactor,
    required this.horizontalSpacingFactor,
    required this.buttonReductionFactor,
  });
}
